#include<stdio.h>
#include<stdlib.h>
void tableMultipilcation1(int start,int end )
{
  int i,j;
  for(i=start;i<=end;i++)
  {
      printf("\n tableMultipilcation de %d :",i);
      for(j=1;j<=12;j++)
      {
          printf("\n %d * %d = %d",i,j,i*j);
          printf("\n");
      }
      printf("\n \n");
  }
}
